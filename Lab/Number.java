import java.util.*;
 
class Number{
    	public static void main(String args[]) {
					
				
		System.out.println("\nDivided by 5: ");
		for (int i=1; i<=100; i++) {          //applying for loop
			if (i%5==0 && i>40) {             // setting conditions 
				
				System.out.print(i +" ");
			}
				
		}
		
				
		System.out.println("\n");
  }
}